    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        int id = Integer.valueOf(tabLocadora.getValueAt(tabLocadora.getSelectedRow(), 0).toString());
                        
        dbLocadora db = new dbLocadora();
        Aluguel rem = db.ConsultaId(id);
        
        jfrmAluguel frm = new jfrmAluguel();
        frm.lblId.setText(String.valueOf((rem.getId())));
        frm.txtMarca.setText(rem.getMarca());
        frm.txtModelo.setText(rem.getModelo());
        frm.txtAno.setText(String.valueOf(rem.getAno()));
        frm.txtValor.setText(String.valueOf(rem.getValor()));
        frm.txtCompleto.setText(rem.getCompleto());
        
        frm.setVisible(true);
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int resp = JOptionPane.showConfirmDialog(this, "Deseja excluir o registro selecionado?", "Exclusao de registro", JOptionPane.YES_NO_OPTION);
                
        
        if(resp == JOptionPane.YES_OPTION){
            int id = Integer.valueOf(tabLocadora.getValueAt(tabLocadora.getSelectedRow(), 0).toString());
                        
            dbLocadora db = new dbLocadora();
            Aluguel rem = db.ConsultaId(id);
            
            db.excluir(rem);
            
            LeGridLocadora();}
    }//GEN-LAST:event_btnExcluirActionPerformed

