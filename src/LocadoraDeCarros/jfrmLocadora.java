    public void LeGridLocadora(){  
        ArrayList<Aluguel> listaLocadora = new ArrayList<Aluguel>();
        dbLocadora db = new dbLocadora();
        
        listaLocadora = db.listar("");
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("Id");
        modelo.addColumn("Marca");
        modelo.addColumn("Modelo");
        modelo.addColumn("Ano");
        modelo.addColumn("Valor");
        modelo.addColumn("Completo");

        Aluguel es;
        
        Iterator ite = listaLocadora.iterator();
        
        while(ite.hasNext()){
            es = (Aluguel)ite.next();
            
            Object[] ob = {es.getId(), es.getMarca(), es.getModelo(), es.getAno(), es.getValor(), es.getCompleto()};
            modelo.addRow(ob);
        }
        
        tabLocadora.setModel(modelo);
    }

